package StudyGroup.Study0516.Example;

import java.util.Scanner;

public class Number {
    int result;

    public int plusTwoNum(int a, int b) {
        result = a + b;
        return result;
    }

    public int minusTwoNum(int firstNum, int lastNum) {
        result = firstNum - lastNum;
        return result;
    }
}
